/*
利用while循环打印1-100的整数
 */
public class 流程_循环_一百17 {
    public static void main(String[] args) {
        int i = 1;
        while(i<=100){
            System.out.println(i);
            ++i;
        }
        System.out.println("程序结束");
    }
}
